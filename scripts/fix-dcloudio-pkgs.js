/**
 * scripts/fix-dcloudio-pkgs.js
 *
 * uni-app 的 alpha 版本 npm 包存在一个已知打包问题：
 * package.json 中 main 字段指向 dist/index.js，但该文件未被打包。
 * 此脚本在 npm install 后自动创建缺失的入口文件。
 */

const fs = require('fs');
const path = require('path');

const PACKAGES = [
  '@dcloudio/uni-app',
  '@dcloudio/uni-mp-weixin',
];

const MISSING_ENTRY = 'dist/index.js';

function fixPackage(pkgName) {
  // 查找包的实际路径（处理 pnpm / npm 的不同目录结构）
  const resolvePkg = (dir) => {
    const pkgDir = path.join(dir, 'node_modules', pkgName);
    if (fs.existsSync(pkgDir)) return pkgDir;
    // pnpm 可能将包放在 .pnpm 目录下
    const pnpmDir = path.join(dir, 'node_modules', '.pnpm');
    if (fs.existsSync(pnpmDir)) {
      const entries = fs.readdirSync(pnpmDir);
      for (const entry of entries) {
        if (entry.startsWith(pkgName.replace('/', '+'))) {
          const inner = path.join(pnpmDir, entry, 'node_modules', pkgName);
          if (fs.existsSync(inner)) return inner;
        }
      }
    }
    return null;
  };

  const pkgDir = resolvePkg(process.cwd()) || resolvePkg(__dirname);

  if (!pkgDir) {
    console.log(`[fix] ${pkgName}: 未找到包，跳过`);
    return;
  }

  const pkgJsonPath = path.join(pkgDir, 'package.json');
  if (!fs.existsSync(pkgJsonPath)) {
    console.log(`[fix] ${pkgName}: package.json 不存在，跳过`);
    return;
  }

  const pkg = JSON.parse(fs.readFileSync(pkgJsonPath, 'utf-8'));
  const mainPath = path.join(pkgDir, pkg.main || MISSING_ENTRY);

  if (fs.existsSync(mainPath)) {
    console.log(`[fix] ${pkgName}: dist/index.js 已存在，无需修复`);
    return;
  }

  // 创建 dist/index.js
  const distDir = path.dirname(mainPath);
  if (!fs.existsSync(distDir)) {
    fs.mkdirSync(distDir, { recursive: true });
  }

  // 检查 uni-app.main 指向的编译器文件，作为后备导出
  const compilerEntry = pkg['uni-app']?.main;
  const content = compilerEntry
    ? `// Auto-generated by scripts/fix-dcloudio-pkgs.js
module.exports = require('./${path.relative(distDir, path.join(pkgDir, compilerEntry))}');
`
    : `// Auto-generated by scripts/fix-dcloudio-pkgs.js
module.exports = {};
`;

  fs.writeFileSync(mainPath, content, 'utf-8');
  console.log(`[fix] ${pkgName}: 已创建缺失的 ${pkg.main || MISSING_ENTRY}`);
}

console.log('[fix] 检查 uni-app 包缺失入口文件...');
PACKAGES.forEach(fixPackage);
console.log('[fix] 完成');
