/**
 * scripts/fix-dcloudio-pkgs.js
 * 修复 @dcloudio/uni-mp-weixin 缺失的 dist/index.js 入口文件
 * uni-app alpha 版本的打包遗漏问题
 */
const fs = require('fs');
const path = require('path');

const pkgDir = path.join(process.cwd(), 'node_modules', '@dcloudio', 'uni-mp-weixin');

if (!fs.existsSync(pkgDir)) {
  console.log('[fix] @dcloudio/uni-mp-weixin 未找到，跳过');
  process.exit(0);
}

const mainPath = path.join(pkgDir, 'dist', 'index.js');

if (fs.existsSync(mainPath)) {
  console.log('[fix] dist/index.js 已存在，无需修复');
  process.exit(0);
}

// 创建 dist/index.js，指向实际的编译器入口
const distDir = path.dirname(mainPath);
if (!fs.existsSync(distDir)) {
  fs.mkdirSync(distDir, { recursive: true });
}

fs.writeFileSync(mainPath, [
  '// Auto-generated by scripts/fix-dcloudio-pkgs.js',
  "module.exports = require('./uni.compiler.js');",
  ''
].join('\n'), 'utf-8');

console.log('[fix] 已创建缺失的 dist/index.js');
